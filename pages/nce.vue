<script setup lang="ts">
// todo 0. 完善切换下一课,下一本书的逻辑
// done 1. 增加 mp3 播放
// todo 2. 拆分句子, 增加间隔
// todo 3. 按键映射
// todo 4. 重构代码
// done 5. 移除 mp3 的 git 同步
